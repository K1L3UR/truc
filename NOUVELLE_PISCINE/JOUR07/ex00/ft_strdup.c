char	*ft_strdup(char *src)
{
	int		i;
	char	*s2;
	int		j;

	j = 0;
	while (src[j] != '\0')
		j++;
	i = 0;
	s2 = (char*)(malloc(sizeof(*s2) * (i + 1)));
	while (j > i)
	{
		s2[i] = src[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
