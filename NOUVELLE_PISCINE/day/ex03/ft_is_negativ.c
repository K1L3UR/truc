#include <unistd.h>

void	ft_is_negativ(int n)
{
	if (n >= 0)
		write (1, "P", 1);
	if (n < 0)
		write (1, "N", 1);
}

int	main()
{
	int n;

	n = 0;	
	ft_is_negativ(n);
	return (0);
}
