char	*ft_strncat(char *dest, char *src, int nb)
{
	int	i;
	int	j;
	int z;

	z = 0;
	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0' && z < nb)
	{
		dest[j + i] = src[j];
		j++;
		z++;
	}
	dest[i + j] = '\0';
	return (dest);
}
